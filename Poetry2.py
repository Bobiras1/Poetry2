# Δέκα μικρά αστικά λυρικά ποιήματα
# Διπλή εκδοχή: ἀρχαῖα ἑλληνικά & νέα ἑλληνικά

poems_ancient = [
    """Ἐν τῇ πλατείᾳ σκιαι λαλῶσι·
παῖς μειδιά·
ἡ πόλις πάλιν ἐγείρεται.""",

    """Φανὸς χλωρὸς ἔλαμψεν·
οὐδεὶς διέβη·
μόνη ὑετός.""",

    """Καφὲς ἐπὶ τοῦ λιθοστρώτου·
καπνὸς ἐκ τῶν καπνῶν·
ἡ ἡμέρα στεναγμῷ ἄρχεται.""",

    """Γραφὴ τοίχων βοᾷ·
φωναὶ ἀπωλώλαν·
ἡ μνήμη χρώμασι γράφει.""",

    """Μοναξία ἐν τῷ μετρω·
αἱ ῥάγαι μέμνηνται
παντὸς βήματος.""",

    """Ἐν τοῖς εξώσταις ἱμάτια κρέμανται·
βίοι ἡμῶν ἀνεμίζουσιν
μετὰ τοῦ ἀνέμου.""",

    """Κεκμήκεν ἡ πόλις·
ἀλλὰ τὰ φῶτα αὐτῆς
ἔτι ᾄδουσιν.""",

    """Ἐν τῇ ὁδῷ ἡ σελήνη
εὗρε κάτοπτρον·
καὶ ἔγένετο δάκρυ.""",

    """Πᾶν θυρίον ποίημα·
πᾶσα κεκλεισμένη αὐλαία
μυστήριον.""",

    """Ἡ πόλις οὐ καθεύδει·
ἀλλ᾽ ἄλλην πλευρὰν στρέφει
ὡς παῖς ἀνήσυχος."""
]

poems_modern = [
    """Στην πλατεία οι σκιές μιλούν·
ένα παιδί χαμογελά·
και η πόλη ξυπνά ξανά.""",

    """Το φανάρι πρασίνισε·
κανείς δεν πέρασε·
μόνο η βροχή.""",

    """Καφές στο πεζοδρόμιο·
καπνός από τσιγάρα·
η μέρα αρχίζει με αναστεναγμό.""",

    """Γκράφιτι στους τοίχους·
φωνές που χάθηκαν·
η μνήμη γράφει με χρώματα.""",

    """Μοναξιά στο μετρό·
μα οι ράγες θυμούνται
κάθε βήμα.""",

    """Στα μπαλκόνια απλωμένα ρούχα·
οι ζωές μας ανεμίζουν
μαζί με τον άνεμο.""",

    """Η πόλη κουράστηκε·
μα τα φώτα της
τραγουδούν ακόμα.""",

    """Στην άσφαλτο η σελήνη
βρίσκει καθρέφτη·
και γίνεται δάκρυ.""",

    """Κάθε παράθυρο κι ένα ποίημα·
κάθε κλειστή κουρτίνα
κι ένα μυστικό.""",

    """Η πόλη δεν κοιμάται·
μόνο αλλάζει πλευρό
σαν ανήσυχο παιδί."""
]

# Εκτύπωση σε ζεύγη
for i, (ancient, modern) in enumerate(zip(poems_ancient, poems_modern), 1):
    print(f"Ποίημα {i} - Ἀρχαῖα:\n{ancient}\n")
    print(f"Ποίημα {i} - Νέα:\n{modern}\n")
    print("="*50 + "\n")
